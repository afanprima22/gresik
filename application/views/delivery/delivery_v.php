<script type="text/javascript">
    $(document).ready(function(){
        search_data();
        select_list_vehicle();
        select_list_driver();
        select_list_helper();
        search_data_detail(0);
    });

    function search_data() { 
        $('#table1').DataTable({
            destroy: true,
            "processing": true,
            "serverSide": true,
            ajax: {
              url: '<?php echo base_url();?>delivery/load_data/'
            },
            "columns": [
              {"name": "delivery_code"},
              {"name": "delivery_date"},
              {"name": "driver"},
              {"name": "helper"},
              {"name": "action","orderable": false,"searchable": false, "className": "text-center"}
            ],
            "order": [
              [0, 'asc']
            ],
            "iDisplayLength": 10
        });
    }

    function search_data_detail(id){
      $("#list_detail").load("<?= base_url()?>Delivery/load_detail/"+id);
    }

    function active_tab(id){
        if (id == 1) {
          $('[href="#tabs-2"]').tab('show');
        }else{
          $('[href="#tabs-1"]').tab('show');
        }
        
    }

    $("#formall").submit(function(event){
        if ($("#formall").valid()==true) {
          action_data();
        }
        return false;
      });

    function action_data(){
        $.ajax({
          type : "POST",
          url  : '<?php echo base_url();?>Delivery/action_data/',
          data : $( "#formall" ).serialize(),
          dataType : "json",
          success:function(data){
            if(data.status=='200'){
              reset();
              reset2();
              search_data();
              $('[href="#list"]').tab('show');
              if (data.alert=='1') {
                document.getElementById('create').style.display = 'block';
                document.getElementById('update').style.display = 'none';
                document.getElementById('delete').style.display = 'none';
              }else if(data.alert=='2'){
                document.getElementById('create').style.display = 'none';
                document.getElementById('update').style.display = 'block';
                document.getElementById('delete').style.display = 'none';
              }
            } 
          }
        });
    }

    function delete_data(id) {
        var a = confirm("Anda yakin ingin menghapus record ini ?");
        if(a==true){
            $.ajax({
                url: '<?php echo base_url();?>Delivery/delete_data',
                data: 'id='+id,
                type: 'POST',
                dataType: 'json',
                success: function (data) {
                  if (data.status=='200') {
                    reset();
                    reset2();
                    search_data();

                    document.getElementById('create').style.display = 'none';
                    document.getElementById('update').style.display = 'none';
                    document.getElementById('delete').style.display = 'block';
                  }
                }
            });
        }
        
    }

    function edit_data(id) {
        $.ajax({
          type : "GET",
          url  : '<?php echo base_url();?>Delivery/load_data_where/',
          data : "id="+id,
          dataType : "json",
          success:function(data){
            for(var i=0; i<data.val.length;i++){
              document.getElementById("i_id").value             = data.val[i].delivery_id;
              document.getElementById("datepicker").value           = data.val[i].delivery_date;
              $("#i_vehicle").append('<option value="'+data.val[i].vehicle_id+'" selected>'+data.val[i].vehicle_name+'</option>');
              $("#i_helper").append('<option value="'+data.val[i].helper+'" selected>'+data.val[i].helper_name+'</option>');
              $("#i_driver").append('<option value="'+data.val[i].driver+'" selected>'+data.val[i].driver_name+'</option>');

              search_data_detail(data.val[i].delivery_id);
            }
          }
        });

        $('[href="#form"]').tab('show');
    }

    function select_list_vehicle() {
        $('#i_vehicle').select2({
          placeholder: 'Pilih Kendaraan',
          multiple: false,
          allowClear: true,
          ajax: {
            url: '<?php echo base_url();?>Vehicle/load_data_select_vehicle/',
            dataType: 'json',
            delay: 100,
            cache: true,
            data: function (params) {
              return {
                q: params.term, // search term
                page: params.page
              };
            },
            processResults: function (data, params) {
              params.page = params.page || 1;

              return {
                results: data.items,
                pagination: {
                  more: (params.page * 30) < data.total_count
                }
              };
            }
          },
          escapeMarkup: function (markup) { return markup; }, // let our custom formatter work
          minimumInputLength: 1,
          templateResult: FormatResult,
          templateSelection: FormatSelection,
        });
      }

